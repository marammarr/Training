package com.jafis.learn.sia.object;

public class ResponseWeb {
	private String response;
	private String description;
	private Integer parameterNilai;
	
	public Integer getParameterNilai() {
		return parameterNilai;
	}
	public void setParameterNilai(Integer parameterNilai) {
		this.parameterNilai = parameterNilai;
	}
	public String getResponse() {
		return response;
	}
	public void setResponse(String response) {
		this.response = response;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	
}
